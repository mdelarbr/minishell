#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	int		a;

	a = 0;
	if (!(dest = malloc(sizeof(char) * (ft_strlen(src) + 1))))
		return (NULL);
	while (src[a] != '\0')
	{
		dest[a] = src[a];
		a++;
	}
	dest[a] = '\0';
	return (dest);
	free(dest);
}
