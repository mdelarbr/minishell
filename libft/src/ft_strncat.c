char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int s;

	i = 0;
	while (dest[i] != '\0')
		i++;
	s = 0;
	while (src[s] != '\0' && s <= nb - 1)
	{
		dest[i + s] = src[s];
		s++;
	}
	dest[i + s] = '\0';
	return (dest);
}
